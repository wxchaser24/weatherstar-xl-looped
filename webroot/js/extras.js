var stationNameFilters = [
	//doing this because some of these piss me off
	["International", "Int'l"],
	["Hartsfield-Jackson ", ""],
]
function formatStationName(stationname) {
	var newstation = stationname;
	for (var i = 0; i < stationNameFilters.length; i++) {
		var ft = stationNameFilters[i];
		newstation = newstation.replaceAll(ft[0], ft[1]);
	}
	if (newstation.includes(" - ")) { newstation = newstation.split(" - ")[1]; }
	return newstation;
}
function getCookie(name){
    let cookies = decodeURIComponent(document.cookie);
    let cookiesArr = cookies.split("; ");
    for(var i = 0; i < cookiesArr.length; i++){
      if(cookiesArr[i].split("=")[0] == name){
        //console.log(cookiesArr[i]);
        return cookiesArr[i].split("=")[1];
      }
    }
    return "";
}
